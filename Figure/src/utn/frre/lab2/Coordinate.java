package utn.frre.lab2;

public class Coordinate {
    private float x;
    private float y;

    public Coordinate(float x, float y) {
        this.setX(x);
        this.setY(y);
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
